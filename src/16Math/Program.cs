int x = 5;
int y = 9;
double z = -7.50;
double k = 7.49;

// Math.Max returns the highest value between two values
Console.WriteLine($"{Math.Max(x, y)}");

// Math.Min returns the smallest value between two values
Console.WriteLine($"{Math.Min(x, y)}");

// Math.Sqrt method returns square root of a value
Console.WriteLine($"{Math.Sqrt(y)}");

// Math.Abs returns absolute positive value
Console.WriteLine($"{Math.Abs(z)}");

// Math.Round returns ceiling or floor value of a decimal number
Console.WriteLine($"{Math.Round(z)}");
Console.WriteLine($"{Math.Round(k)}");

