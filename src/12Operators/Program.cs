// = operator is used to assign a value to a variable
int x = 50;
int y = 60;

// + operator is used to add together two values
int sum = x + y;

// - operator is used to subtruct one value from another
int sub = x - y;

// * is used for multiplies two values
int mult = x * y;

// / is used for divides one value by another
int div = x / y;

// % is returns the division remainder
int remainder = x % y;

// ++ increases the value by 1
int increment = x++;

// -- decreases the value by 1
int decrement = y--;