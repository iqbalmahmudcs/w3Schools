/*
 * The backslash (\) escape character turns special characters into string characters:
 */

//  \'	' Single quote
string txt = "We are the so-colled \"Vikings\" from the north";
Console.WriteLine(txt);

//  \"	"	Double quote
txt = "It's alright";
Console.WriteLine(txt);

//  \\	\	Backslash
txt = "The charecter \\ is called backslash";
Console.WriteLine(txt);