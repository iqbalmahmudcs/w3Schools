//To declare multiple variables in a line use comma-seperated list:
using System.Runtime.InteropServices;

int x = 5, y = 6, z = 10;
Console.WriteLine(x + y + z);

//Assign the same value to multiple variables in a single line:
x = y = z = 3;
Console.WriteLine(x);
Console.WriteLine(y);
Console.WriteLine(z);