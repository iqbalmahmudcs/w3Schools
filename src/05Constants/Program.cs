/*
 * If you don't want to change the value of a word of the next line of code you can declare a variable a constant variable. 
 * For that you just add const keyword in front of that variable.
 * This will declare the variable as constant, which is unchangeable and read-only. 
 */

int myNumber = 5;    //changeable
Console.WriteLine(myNumber);
myNumber = 6;   //you can change the value
Console.WriteLine(myNumber);

const double pi = 3.1416D;    //Unchangeable
Console.WriteLine(pi);
//pi = 5.5D;    //doesnot allow you to change the value



