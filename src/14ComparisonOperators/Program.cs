/*
 * Comparison operators are used for compare two variables or values.
 * It returns true of false value.
 */

int x = 5;
int y = 6;

// == Equal to Operators
Console.WriteLine(x == y);

// != Not Equal to Operators
Console.WriteLine(x != y);

// > is used for finding the greater value than another.
Console.WriteLine(x > y);

// < is used for finding the smaller value than another.
Console.WriteLine(x < y);

// > is used for finding the Greater than or equal to another.
Console.WriteLine(x >= y);

// < is used for finding the smaller than or equal to another.
Console.WriteLine(x <= y);