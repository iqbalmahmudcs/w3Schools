/*
 * In C#, there are different types of variables (defined with different keywords), for example:
 * int - stores integers (whole numbers), without decimals, such as 123 or -123
 * double - stores floating point numbers, with decimals, such as 19.99 or -19.99
 * char - stores single characters, such as 'a' or 'B'. Char values are surrounded by single quotes
 * string - stores text, such as "Hello World". String values are surrounded by double quotes
 * bool - stores values with two states: true or false
 */

string name = "Iqbal";
Console.WriteLine(name);

int myNumber = 10;
Console.WriteLine(myNumber);

int num; //we can declare a variable without assigning a value.
num = 6;
Console.WriteLine(num);

myNumber = 20;    //myNumber is replaced 10 by 20
Console.WriteLine(myNumber);

double myDoubleNumber = 5.5D;
char myCharecter = 'D';
bool myBool = true;
string myText = "this is a text!";

Console.WriteLine(myDoubleNumber);
Console.WriteLine(myCharecter);
Console.WriteLine(myBool);
Console.WriteLine(myText);

