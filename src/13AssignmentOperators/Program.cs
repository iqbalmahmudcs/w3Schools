// = Assignment Operators
int x = 5;
Console.WriteLine(x);

// += is used to add together two values and assign it
x += 5;
Console.WriteLine(x);

// -= is used to subtruct one value from another and assign it
x -= 2;
Console.WriteLine(x);

// *= is used to multiply two values and assign it
x *= 10;
Console.WriteLine(x);

// /= is used to divides one valu to another and assign it
x /= 5;
Console.WriteLine(x);

// %= is returns the division remainder and assign it
x %= 5;
Console.WriteLine(x);

// &= is used for and operation between two numbers and assign it
x &= 5;
Console.WriteLine(x);


// |= is used for Bitwise OR assignment
x |= 5;
Console.WriteLine(x);

// ^= is used for Bitwise XOR assignment
x ^= 5;
Console.WriteLine(x);

// >>= is used for Right Shift assignment
x >>= 5;
Console.WriteLine(x);

// <<= is used Left Shift assignment
x <<= 5;
Console.WriteLine(x);

